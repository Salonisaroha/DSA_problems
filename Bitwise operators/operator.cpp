#include<iostream>
using namespace std;
int main(){
    // & operator
    cout<<(2&5)<<endl;
    // | operator

    cout<<(10|90)<<endl;
    // ^ operator

    cout<<(2^4)<<endl;

    // not operator
    cout<<(~9);

    // Left shift

    cout<<(7<<2)<<endl; // formula for left shift is (a<<b) => a*2^b

    // right shift
    cout<<(7>>2)<<endl;


}